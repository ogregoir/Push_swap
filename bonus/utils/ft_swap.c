#include "../push_swap_bonus.h"

void	ft_swap(t_list **i)
{
	t_list	*temp;

	if (!*i)
		return ;
	temp = (*i)->next;
	(*i)->next = (*i)->next->next;
	temp->next = (*i);
	(*i) = temp;
}
